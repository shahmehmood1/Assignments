function increment(value1){
  value1 = value1.substring(value1.indexOf("_")+1,value1.length)
  //console.log(value1)
  //cartData.filter(food=>food.id==value1)
  var itemtoinc = cartData.find(item=>item.id==value1)
  //console.log(itemtoinc)
  itemtoinc.quantity += 1
  //itemtoinc.price = (itemtoinc.quantity) * 
  //30 - 15 / 1
  var curprice = ((itemtoinc.price * itemtoinc.quantity) - (itemtoinc.price * (itemtoinc.quantity - 1)))/( itemtoinc.quantity - 1);
  //console.log(curprice)
  itemtoinc.price = curprice * itemtoinc.quantity
  //console.log(itemtoinc.price)
  totalprice();
  displayCart();
  }
  
function decrement(value1){
    //console.log('decrementing')
    value1 = value1.substring(value1.indexOf("_")+1,value1.length)
    var itemtodec = cartData.find(item=>item.id==value1)
    var idx = cartData.indexOf(itemtodec)
    console.log(itemtodec)
    if(itemtodec.quantity>1){
      var curprice = ((itemtodec.price * (itemtodec.quantity+1)) - (itemtodec.price * itemtodec.quantity))/( itemtodec.quantity);
      itemtodec.quantity -= 1
      //console.log(curprice)
      itemtodec.price = curprice * itemtodec.quantity
    }else{
      cartData.splice(idx,1)
      var cart = document.getElementById('cart-plus')
      cart.innerText = ` ${cartData.length} items`
      if (cartData.length == 0) {
        ele.style = "display:block"
        cartpage.style = "display : none"
        alert('All items have been removed! Going to homepage')
        //totalprice();
        displayItem('biryani')
        return       
      }
    }
      totalprice();
      displayCart();
  }

